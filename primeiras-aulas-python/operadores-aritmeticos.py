produto_1 = 10
produto_2 = 20

print(produto_1 + produto_2) #adicao
print(produto_1 - produto_2) #subtracao
print(produto_1 / produto_2) #divisao flutuante
print(produto_1 // produto_2) #divisao inteira
print(produto_1 * produto_2) #multiplicacao
print(produto_1 ** produto_2) #potencia
print(produto_1 % produto_2) #modulo -> resto da divisao

